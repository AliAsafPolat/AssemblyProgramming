TITLE DIZININ ELEMANLARI TOPLAMI

DATASG SEGMENT PARA 'DATA'
	DIZI DW 1,6,8,9,2,3,4,5,6,10
	ELEMAN DW 10
	RESULT DD 0
DATASG ENDS

STACKSG SEGMENT PARA STACK 'STACK'
		DW 20 DUP(?)
STACKSG ENDS		

CODESG SEGMENT PARA 'CODE'
	
BASLA PROC FAR 
		ASSUME CS:CODESG,SS:STACKSG,DS:DATASG
		
		PUSH DS
		XOR AX,AX
		PUSH AX
		
		MOV AX,DATASG
		MOV DS,AX
		
		XOR SI,SI
		MOV CX,ELEMAN
		
LABEL1: 

			MOV AX,DIZI[SI]
			ADD WORD PTR[RESULT],AX
			ADC WORD PTR[RESULT+2],0
			
			ADD SI,2
			LOOP LABEL1
		
		
		
		RETF
	BASLA ENDP
CODESG ENDS
END BASLA	