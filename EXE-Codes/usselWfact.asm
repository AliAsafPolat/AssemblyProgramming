TITLE 16 BITLIK SAYI TOPLAMA

STACKSG SEGMENT PARA STACK 'STACK'

	DW 10 DUP (?)
STACKSG ENDS

DATASG SEGMENT PARA 'DATA'

KSAYISI DW 1 
CSAYISI DW 1
TOPLAM  DD 0
N		DW 4
XSAYISI DW 6
DATASG ENDS

CODESG SEGMENT PARA 'CODE'
	ASSUME CS:CODESG,DS:DATASG,SS:STACKSG
BASLA PROC FAR

		PUSH DS
		XOR AX,AX
		PUSH AX

		MOV AX,DATASG
		MOV DS,AX
		XOR DI,DI
		MOV SI,1

		MOV CX,N
		DEC CX
L1:		MOV BX,KSAYISI
		PUSH CX
		MOV CX,SI
		MOV DI,1
L2:		
		XOR DX,DX
		MOV AX,BX
		MUL XSAYISI
		MOV BX,AX
		
		XOR DX,DX
		MOV AX,CSAYISI
		MUL DI
		MOV CSAYISI,AX
		
		INC DI
		LOOP L2
		
		MOV AX,BX
		DIV CSAYISI
		
		ADD WORD PTR[TOPLAM],AX
		ADC WORD PTR[TOPLAM+2],0
		MOV CSAYISI,1
		
		INC SI
		POP CX
		LOOP L1
		
		INC WORD PTR [TOPLAM]
		

RETF



BASLA ENDP
CODESG ENDS
END BASLA
