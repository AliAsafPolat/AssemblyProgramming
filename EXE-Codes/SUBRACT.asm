TITLE İKİ SAYININ FARKI

DATASG SEGMENT PARA 'DATA'
	SAYI1 DW 15
	SAYI2 DW 10
	RESULT DW 0
DATASG ENDS

STACKSG SEGMENT PARA STACK 'STACK'
		DW 20 DUP(?)
STACKSG ENDS		

CODESG SEGMENT PARA 'CODE'
	
BASLA PROC FAR 
		ASSUME CS:CODESG,SS:STACKSG,DS:DATASG
		
		PUSH DS
		XOR AX,AX
		PUSH AX
		
		MOV AX,DATASG
		MOV DS,AX
		
		XOR AX,AX
		
		MOV AX,SAYI1
		MOV BX,SAYI2
		
		SUB AX,BX
		MOV RESULT,AX
		
		
		
		RETF
	BASLA ENDP
CODESG ENDS
END BASLA	