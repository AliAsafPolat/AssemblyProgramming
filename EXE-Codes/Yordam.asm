CODESG SEGMENT PARA 'CODE'
	ASSUME CS:CODESG,SS:STACKSG,DS:DATASG
	
BASLA PROC FAR
	PUSH DS 
	XOR AX,AX
	PUSH AX
	
	MOV AX,DATASG
	MOV DS,AX
	
	MOV CX,UST
	MOV BX,SAYI
