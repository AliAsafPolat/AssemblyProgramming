BASLA PROC FAR 
			PUSH DS
			XOR AX,AX
			PUSH AX
			
			MOV AX,DATASG
			MOV DS,AX
			
			XOR SI,SI
			XOR DI,DI
			
			MOV CX,ELEMAN
LABEL1:		
			PUSH CX 
			;MOV CX,ELEMAN
LABEL2: 					
			MOV AX,DIZI[DI]
			CMP AX,DIZI[DI+2]
			JNG BUYUKDEGIL
			
			MOV BX,DIZI[DI+2]
			MOV DIZI[DI],BX
			MOV DIZI[DI+2],AX
			
BUYUKDEGIL: 
			ADD DI,2 
			LOOP LABEL2
			
			POP CX 
			XOR DI,DI
			ADD SI,2
			LOOP LABEL1

		RETF 
	BASLA ENDP
	CODESG ENDS
	END BASLA
	



			