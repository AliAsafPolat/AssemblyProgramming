TITLE UZAKTAN YORDAM CAGIRILMASI
EXTRN TOPLAMA:FAR


STACKSG SEGMENT PARA STACK 'STACK'
	DW 20 DUP(?)
STACKSG ENDS

DATASG SEGMENT PARA 'DATA'
		SAYI1 DB 12H
		SAYI2 DB 78H
		SONUC DW 0
DATASG ENDS

CODESG SEGMENT PARA 'CODE'
			ASSUME SS:STACKSG,DS:DATASG,CS:CODESG
	
	BASLA PROC FAR
		PUSH DS
		XOR AX,AX
		PUSH AX
		
		MOV AX,DATASG
		MOV DS,AX
		
		MOV BH,SAYI1
		MOV BL,SAYI2
		
		CALL TOPLAMA
		MOV SONUC,AX
	
RETF	
BASLA ENDP
CODESG ENDS
END BASLA