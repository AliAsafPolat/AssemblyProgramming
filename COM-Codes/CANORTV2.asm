CODESG SEGMENT PARA 'CODE'
	ORG 100H
	ASSUME CS:CODESG,SS:CODESG,DS:CODESG,ES:CODESG
	
	BILGI :JMP BASLA
	DIZI 	 DB 50,50,40,40,60,30,60,30,20,20
	ELEMAN	 DB 10
	ORTALAMA DB 0
	ESIK 	 DB 40
	ESIKUSTU DW 0
	ESIKSAY  DB 0
	BUT		 DB 0
	BASARILI DB 0
	KALDI 	 DB 0

BASLA PROC NEAR
	
			XOR SI,SI
			LEA SI,DIZI
			MOV CL,ELEMAN
			XOR AX,AX

L1: 
			MOV AL,[SI]
			CMP AL,ESIK
			JAE KALMADI
			INC KALDI
			JMP SON
KALMADI:	
			INC ESIKSAY
			CBW 
			ADD ESIKUSTU,AX
			
SON:		INC SI
			LOOP L1
			
			XOR AX,AX
			XOR DX,DX
			MOV AX,ESIKUSTU
			DIV ESIKSAY
			MOV ORTALAMA,AL
			
			XOR SI,SI
			MOV CL,ELEMAN
			LEA SI,DIZI
			XOR AX,AX
			
L2:			MOV AL,[SI]
			CMP AL,ORTALAMA
			JB	BUTE 
			INC BASARILI
	BUTE :	
			INC SI
			LOOP L2
			
			XOR AX,AX
			MOV AL,ESIKSAY
			SUB AL,BASARILI
			MOV BUT,AL
			
			
			RET
		BASLA ENDP
CODESG ENDS
END BILGI		
			
			
			
			
			
			
	