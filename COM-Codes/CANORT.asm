CODESG SEGMENT PARA 'CODE'
	ORG 100H
	ASSUME CS:CODESG,SS:CODESG,DS:CODESG,ES:CODESG
	
	BILGI :JMP BASLA
	DIZI 	 DW 50,50,40,40,60,30,60,30,20,20
	ELEMAN	 DW 10
	ORTALAMA DW 0
	ESIK 	 DW 40
	ESIKUSTU DW 0
	ESIKSAY  DW 0
	BUT		 DW 0
	BASARILI DW 0
	KALDI 	 DW 0

BASLA PROC NEAR
	
			XOR SI,SI
			LEA SI,DIZI
			MOV CX,ELEMAN

L1: 
			MOV AX,[SI]
			CMP AX,ESIK
			JAE KALMADI
			INC KALDI
			JMP SON
KALMADI:	
			INC ESIKSAY
			ADD ESIKUSTU,AX
			
SON:		ADD SI,2
			LOOP L1
			
			XOR AX,AX
			XOR DX,DX
			MOV AX,ESIKUSTU
			DIV ESIKSAY
			MOV ORTALAMA,AX
			
			XOR SI,SI
			MOV CX,ELEMAN
			LEA SI,DIZI
			
L2:			MOV AX,[SI]
			CMP AX,ORTALAMA
			JB	BUTE 
			INC BASARILI
	BUTE :	
			ADD SI,2
			LOOP L2
			
			XOR AX,AX
			MOV AX,ESIKSAY
			SUB AX,BASARILI
			MOV BUT,AX
			
			
			RET
		BASLA ENDP
CODESG ENDS
END BILGI		
			
			
			
			
			
			
	